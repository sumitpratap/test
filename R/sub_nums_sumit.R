#' @title Substraction program

#' @description Substraction of 2 numbers

#' @param 
#'	arg1 first argument
#' @param
#'	arg2 second argument

#' @examples 
#'	sub_nums_sumit(12,6)
#'	sub_nums_sumit (50,70)

#' @return
#'	This function will return the substraction of the first 	argument given to the second argument

#' @references Not Applicable

#' @export



sub_nums_sumit <- function(arg1, arg2) {
    arg1 - arg2
}